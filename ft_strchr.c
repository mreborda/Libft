char	*ft_strchr(const char *str, int c)
{	
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (c == str[i])
			return ((char *)str + i);
		i++;
	}	
	if (c == '\0')
		return ((char *)str);
	return (0);
}
